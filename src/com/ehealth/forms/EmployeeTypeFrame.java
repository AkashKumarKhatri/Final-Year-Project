    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if(txtEmployeeType.getText().trim().isEmpty() == true) {
            JOptionPane.showMessageDialog(rootPane, "Please Enter RoomType");
        }
        else {
            EmployeeTypeModel employeeTypeModel = new EmployeeTypeModel();
            employeeTypeModel.setEmployeeType(txtEmployeeType.getText());
            EmployeeTypeDAO employeeTypeDAO = new EmployeeTypeDAOImpl();
            if(employeeTypeDAO.isAvailableEmployeeType(employeeTypeModel)==true) {
                JOptionPane.showMessageDialog(rootPane, "Employee Type is Already exist");
            }
            else {
                int row = employeeTypeDAO.addEmployeeType(employeeTypeModel);
                if(row>0) {
                    txtEmployeeType.setText(null);
                    populateMyTable();
                }
                else {
                    JOptionPane.showMessageDialog(null, "Record Not added");
                }
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if(txtEmployeeType.getText().trim().isEmpty() == true) {
            JOptionPane.showMessageDialog(rootPane, "Please Enter RoomType");
        }
        else {
            EmployeeTypeModel employeeTypeModel = new EmployeeTypeModel();
            employeeTypeModel.setEmployeeType(txtEmployeeType.getText());
            employeeTypeModel.setEmployeeTypeId(employeeTypeId);
            EmployeeTypeDAO employeeTypeDAO = new EmployeeTypeDAOImpl();
            if(employeeTypeDAO.isAvailableEmployeeType(employeeTypeModel)==true) {
                JOptionPane.showMessageDialog(rootPane, "Employee Type is Already exist");
            }
            else {
                int row = employeeTypeDAO.updateEmployeeType(employeeTypeModel);
                if(row>0) {
                    txtEmployeeType.setText(null);
                    populateMyTable();
                    btnAdd.setEnabled(true);
                    btnDelete.setEnabled(false);
                    btnUpdate.setEnabled(false);
                }
                else {
                    JOptionPane.showMessageDialog(null, "Record Not Updated");
                }
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        Integer confirm = JOptionPane.showConfirmDialog(rootPane, "Do you realy want to delete", "Delete", JOptionPane.YES_NO_OPTION);
        if(confirm == 0) {
            EmployeeTypeModel employeeTypeModel = new EmployeeTypeModel();
            employeeTypeModel.setEmployeeTypeId(employeeTypeId);
            EmployeeTypeDAO employeeTypeDAO = new EmployeeTypeDAOImpl();
            int row = employeeTypeDAO.deleteEmployeeType(employeeTypeModel);
            if(row>0) {
                txtEmployeeType.setText(null);
                populateMyTable();
                btnAdd.setEnabled(true);
                btnDelete.setEnabled(false);
                btnUpdate.setEnabled(false);
            }
            else {
                JOptionPane.showMessageDialog(null, "Record Not Deleted");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

