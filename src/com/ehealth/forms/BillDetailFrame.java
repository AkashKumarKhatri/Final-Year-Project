    private void loadReport(){
        String source = "C:/Users/Akash/Documents/NetBeansProjects/03_E-Health-Solutions/Patient_Bill.jrxml";
        Map<String, Object> parameters = new HashMap<String, Object>();
        try {
            parameters.put("apId", appointmentId);
            JasperReport report = JasperCompileManager.compileReport(source);
            JasperPrint filledReport = JasperFillManager.fillReport(report, parameters,DBConnection.getConnection());
            JDialog dialog = new JDialog();
            dialog.add(new JRViewer(filledReport));
            dialog.setSize(983,752);
            dialog.setVisible(true);
        } catch (JRException ex) {
            System.out.println("Exception in loadReport Method in class BillDetailFrame");
        }
    }
