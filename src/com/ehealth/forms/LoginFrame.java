    private void lbLoginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lbLoginMouseClicked
        lbLogin.requestFocus();
        if((txtUsername.getText().trim().equals("USERNAME")) || (txtPassword.getText().trim().equals("PASSWORD"))
                || (txtPassword.getText().trim().isEmpty()) || (txtUsername.getText().trim().isEmpty())) {
            lbAlert.setText("*Please Enter Username and Password");
        }
        else {
            UserDAO userDAO = new UserDAOImpl();
            UserModel userModel = userDAO.getUser(txtUsername.getText().trim(), txtPassword.getText().trim());
            if(userModel!=null) {
                this.dispose();
                userType = userModel.getUserTypeModel().getUserType();            
                userName = userModel.getName();
                new MainFrame().setVisible(true);
            }
            else {
                lbAlert.setText("Please Try Again");
                txtPassword.setText(null);
                txtUsername.setText(null);
            }
        }
        
    }//GEN-LAST:event_lbLoginMouseClicked

