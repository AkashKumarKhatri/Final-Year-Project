    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if(txtBloodGroup.getText().trim().isEmpty() == true) {
            JOptionPane.showMessageDialog(rootPane, "Please Enter Blood Group");
        }
        else {
            BloodModel bloodModel = new BloodModel();
            bloodModel.setBloodGroup(txtBloodGroup.getText());
            BloodDAO bloodDAO = new BloodDAOImpl();
            if(bloodDAO.isAvailableBloodGroup(bloodModel) == true) {
                JOptionPane.showMessageDialog(rootPane, "Blood Group is Already exist");
            }
            else {
                int row = bloodDAO.addBloodGroup(bloodModel);
                if(row>0) {
                    txtBloodGroup.setText(null);
                    populateMyTable();
                }
                else {
                    JOptionPane.showMessageDialog(null, "Not Added");
                }
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if(txtBloodGroup.getText().trim().isEmpty() == true) {
            JOptionPane.showMessageDialog(rootPane, "Please Enter Blood Group");
        }
        else {
            BloodModel bloodModel = new BloodModel();
            bloodModel.setBloodGroup(txtBloodGroup.getText());
            bloodModel.setBloodId(bloodId);
            BloodDAO bloodDAO = new BloodDAOImpl();
            if(bloodDAO.isAvailableBloodGroup(bloodModel) == true) {
                JOptionPane.showMessageDialog(rootPane, "Blood Group is Already exist");
            }
            else {
                int row = bloodDAO.updateBloodGroup(bloodModel);
                if(row>0) {
                    txtBloodGroup.setText(null);
                    populateMyTable();
                    btnAdd.setEnabled(true);
                    btnDelete.setEnabled(false);
                    btnUpdate.setEnabled(false);
                }
                else {
                    JOptionPane.showMessageDialog(null, "Not Updated");
                }
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        Integer confirm = JOptionPane.showConfirmDialog(rootPane, "Do you realy want to delete", "Delete", JOptionPane.YES_NO_OPTION);
        if(confirm == 0) {
            BloodModel bloodModel = new BloodModel();
            bloodModel.setBloodId(bloodId);
            BloodDAO bloodDAO = new BloodDAOImpl();
            int row = bloodDAO.deleteBloodGroup(bloodModel);
            if(row>0) {
                populateMyTable();
                txtBloodGroup.setText(null);
            }
        }
        else {
            JOptionPane.showMessageDialog(null, "Record Not Deleted");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

